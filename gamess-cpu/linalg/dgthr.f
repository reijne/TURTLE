      subroutine dgthr(n,a,r,map)
      implicit double precision  (a-h,p-w),integer (i-n),logical    (o)
      implicit character *8 (z),character *1 (x)
      implicit character *4 (y)
      dimension r(*),a(*),map(*)
      if(n.gt.0)then
      do 1 loop=1,n
    1 r(loop)=a(map(loop))
      endif
      return
      end
